    private boolean sendEmail(NhanVien nv) {
        codeSMS =  String.format("%.0f",(Math.random() * (999999 - 111110 + 1) + 111110));
        final String username = "sendmail.fpttext@gmail.com";
        final String password = "sendmail.fpttext10";

        Properties prop = new Properties();
        prop.put("mail.smtp.host", "smtp.gmail.com");
        prop.put("mail.smtp.port", "587");
        prop.put("mail.smtp.auth", "true");
        prop.put("mail.smtp.starttls.enable", "true"); //TLS

        Session session;
        session = Session.getInstance(prop, new javax.mail.Authenticator() {
            @Override
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(username, password);
            }
        });

        try {

            Message message = new MimeMessage(session);
            message.setFrom(new InternetAddress("sendmail.fpttext@gmail.com"));
            message.setRecipients(
                    Message.RecipientType.TO,
                    InternetAddress.parse(txtEmail.getText())
            );
            message.setSubject("HE THONG DAO TAO EDUSYS");
            message.setText("Chào " + nv.getHoTen() + " \n"
                    + "Mã OTP: " + codeSMS);

            Transport.send(message);

            MessegerHelper.alert("Chúng tôi đã gửi mã OTP đến địa chỉ Email trên, \nBạn vui lòng kiểm tra hộp thư!", this);

        } catch (MessagingException e) {
            e.printStackTrace();
            MessegerHelper.alert("Gửi mail thất bại", this);
            return true;
            
        }
        return false;
    }
