    private void btnDongYDoiMKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDongYDoiMKActionPerformed
        if (txtMatKhauMoi.getText().trim().isEmpty()){
            MessegerHelper.errorMesseger(new StringBuilder("Bạn phải nhập mật khẩu mới"), this);
            return; 
        } else if (txtMatKhauMoi2.getText().trim().isEmpty()){
            MessegerHelper.errorMesseger(new StringBuilder("Bạn phải nhập lại mật khẩu mới"), this);
            return; 
        } else if (!txtMatKhauMoi2.getText().equals(txtMatKhauMoi.getText())){
            MessegerHelper.errorMesseger(new StringBuilder("Mật khẩu xác nhận không trùng nhau"), this);
            return; 
        }
        this.nv.setMatKhau(txtMatKhauMoi.getText());
        try {
            this.dao.update(nv);
            MessegerHelper.alert("Đổi mật khẩu thành công\nXin mời bạn đăng nhập lại", this);
            this.dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
            MessegerHelper.errorMesseger(new StringBuilder("Lỗi truy vấn"), this);
            return; 
        }
    }//GEN-LAST:event_btnDongYDoiMKActionPerformed

