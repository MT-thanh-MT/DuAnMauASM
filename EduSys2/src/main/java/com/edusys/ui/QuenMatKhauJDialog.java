    private void btnDongYActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDongYActionPerformed
        //checkRong
        StringBuilder loi = new StringBuilder();
        String manv = txtTaiKhoan.getText();
        String Email = txtEmail.getText();
        // check mã nhân viên
        if (manv.trim().isEmpty()) {
            loi.append("Không được để trống mã nhân viên\n");
        } else if (manv.length() > 50) {
            loi.append("Mã nhân viên không được quá 50 ký tự\n");
        }

        //check Email
        Pattern pEmail = Pattern.compile("^\\w+@+[a-zA-Z]+(\\.[a-zA-Z]+){1,2}$");
        if (Email.trim().isEmpty()) {
            loi.append("Không được để trống Email\n");
        } else if (!pEmail.matcher(Email).find()) {
            loi.append("Email phải đúng định dạng!\nVD: ...@gmail.com hoặc ...@fpt.edu.vn\n");
        }

        //hiển thị thông báo lỗi
        if (loi.length() > 0) {
            MessegerHelper.errorMesseger(loi, this);
            return;
        }
        nv = new NhanVien();
        try {
            nv = this.dao.selectByID(manv);
            if (nv == null) {
                MessegerHelper.errorMesseger(new StringBuilder("Tài khoản này không tồn tại!"), this);
                return;
            } else if (!nv.getEmail().equals(Email)) {
                MessegerHelper.errorMesseger(new StringBuilder("Sai email đăng ký!"), this);
                return;
            }
        } catch (Exception ex) {
            MessegerHelper.errorMesseger(new StringBuilder("Lỗi truy vấn"), this);
            return;
        }

        if (sendEmail(nv)) return;
        
        CardLayout layout = (CardLayout) pnlCardsQMK.getLayout();

        layout.show(pnlCardsQMK, "MaOTP");

    }//GEN-LAST:event_btnDongYActionPerformed

